:-dynamic(varNumber / 3).
symbolicOutput(0).  % set to 1 to see symbolic output only; 0 otherwise.

%% A (6-side) "letter dice" has on each side a different letter.  Find
%% four dice, with the 24 letters abcdefghijklmnoprstuvwxy appearing
%% in exactly one dice such that you can make all the following words:
%% bake, onyx, echo, oval, gird, smug, jump, torn, luck, viny, lush,
%% wrap.


%%%%%%%%%%%%%%%%%%%% input %%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%%
letter(L):-member(L,[a,b,c,d,e,f,g,h,i,j,k,l,m,n,o,p,r,s,t,u,v,w,x,y]).
word( [b,a,k,e] ).
word( [o,n,y,x] ).
word( [e,c,h,o] ).
word( [o,v,a,l] ).
word( [g,i,r,d] ).
word( [s,m,u,g] ).
word( [j,u,m,p] ).
word( [t,o,r,n] ).
word( [l,u,c,k] ).
word( [v,i,n,y] ).
word( [l,u,s,h] ).
word( [w,r,a,p] ).

%%%%%% Some helpful definitions to make the code cleaner:
dice(D):-between(1,4,D).
side(S):-between(1,6,S).
letterWord(L) :- word(X), member(L, X).
%differentletters(S1, S2) :- side(S1), side(S2), letter(L), member(S1).

%%%%%%  SAT Variables:

% x(D,S,L) means "dice D, side S, gets letter L"
satVariable( x(D,S,L) ):- dice(D), side(S), letter(L).
satVariable( x(D,S)) :- dice(D), side(S).
satVariable( x(D,L)) :- dice(D), letter(L).

writeClauses:- 
    inputWords,
    eachDiceSideExactly1Letter,
    
    
    true,!.                    % this way you can comment out ANY previous line of writeClauses
writeClauses:- told, nl, write('writeClauses failed!'), nl,nl, halt.

inputWords :- letterWord(L), findall(x(D, S, L), (side(S), dice(D)), Lits), writeClause(Lits), fail.
inputWords.

eachDiceSideExactly1Letter :- dice(D), side(S), findall(x(D, S, L), letter(L), Lits), exactly(1, Lits), fail.
eachDiceSideExactly1Letter.

%%%%%%%%%%%%%%%%%%%%%%%
%%%%%% show the solution. Here M contains the literals that are true in the model:

displaySol(M):-
    dice(D), write('Dice '), write(D), write(': '),
    findall(L,member(x(D,_,L),M),Faces), write(Faces), nl, fail.
displaySol(_).

